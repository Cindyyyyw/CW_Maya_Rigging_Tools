def curve_data_to_dict(curveDict, curveName):
    cmds.createNode( 'curveInfo' , n='temp_curveInfo')
    cmds.connectAttr( curveName +'.worldSpace', 'temp_curveInfo.inputCurve' )
    curveDictItem = {
        'cv': cmds.getAttr( 'temp_curveInfo.controlPoints[*]' ),
        'knot': cmds.getAttr( 'temp_curveInfo.knots[*]' )
    }
    cmds.delete('temp_curveInfo')
    
    curveDict[curveName] = curveDictItem
    return curveDict


